# In python, there are many types of data:
# Strings, lines of texts:
"String example"

"Other string"
# Integers (whole numbers):
9
# Floats (decimal numbers):
2.9
7.18
# Booleans (binary values, can be either true or false):
True
False
# You can print them with the print() function:
print(True)
print(2.9)
print(7)
print("hello world")
# Or you can assign them to variables:
intvar = 8
strvar = "hello world"
boolvar = False
floatvar = 11.98

# You can then print the variables:
print(intvar, strvar, boolvar, floatvar)
